fun main() {
    val nome: String = "Leonardo Fernandes" // OBJETO=tipo
    val tamanhoNome = nome.length // função que acessa a propriedade de um objeto (String)
    println(tamanhoNome)
    val maiuscula = nome.uppercase() // função que muda um comportamento de um objeto (String)
    val minuscula = nome.lowercase()
    println(maiuscula)
    println(minuscula)
    println("Olá Mundo".length)
}